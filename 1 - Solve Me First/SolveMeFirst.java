/**
 * Solve Me First
 *
 * Time complexity:
 * Worst case: Θ(1)
 * Best case: Θ(1)
 *
 * Space complexity:
 * Worst case: Θ(1)
 * Best case: Θ(1)
 *
 * @author Saksham Tiwari
 */

public class SolveMeFirst {
    /**
     * Function that adds two numbers.
     * @param a Integer one.
     * @param b Integer two.
     * @return Sum of the integers a and b.
     */
    static int solveMeFirst(final int a, final int b) {
        return a + b;
    }
}
